    public void progressBar(){
        levelUp = rank * multiplier;
        if(exp >= levelUp){
            rank = rank -1;
            jLabelRankIcon.setText("" + rank);
            multiplier = multiplier * 2;
            jLabelExp.setText("" + exp +"/" + levelUp);
        }
        levelUp = rank * multiplier;
        progress = exp/levelUp * 100;
       
    }
