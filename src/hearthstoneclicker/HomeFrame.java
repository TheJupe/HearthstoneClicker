    class GoldPerSecond extends Thread{
        public void run(){
            do{
              gold = gold + goldPerSecond;
              exp = exp + goldPerSecond;
              jLabelGoldAmt.setText("" + gold);
                try {
                    sleep(1000);
                } catch (InterruptedException ex) {
                    Logger.getLogger(HomeFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }while(true);
        }
        
    }
    

